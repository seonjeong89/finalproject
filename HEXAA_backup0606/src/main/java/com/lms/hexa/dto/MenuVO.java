package com.lms.hexa.dto;

public class MenuVO {
	
	private String menuId;
	private String menuName;
	private String menuUrl;
	private String menuIcon;
	private String menuUpcode;
	private String menuLevel;
	private String menuCategory;
	private String menuHaschild;
	private String temp;
	
	public String getMenuId() {
		return menuId;
	}
	public void setMenuId(String menuId) {
		this.menuId = menuId;
	}
	public String getMenuName() {
		return menuName;
	}
	public void setMenuName(String menuName) {
		this.menuName = menuName;
	}
	public String getMenuUrl() {
		return menuUrl;
	}
	public void setMenuUrl(String menuUrl) {
		this.menuUrl = menuUrl;
	}
	public String getMenuIcon() {
		return menuIcon;
	}
	public void setMenuIcon(String menuIcon) {
		this.menuIcon = menuIcon;
	}
	public String getMenuUpcode() {
		return menuUpcode;
	}
	public void setMenuUpcode(String menuUpcode) {
		this.menuUpcode = menuUpcode;
	}
	public String getMenuLevel() {
		return menuLevel;
	}
	public void setMenuLevel(String menuLevel) {
		this.menuLevel = menuLevel;
	}
	public String getMenuCategory() {
		return menuCategory;
	}
	public void setMenuCategory(String menuCategory) {
		this.menuCategory = menuCategory;
	}
	public String getMenuHaschild() {
		return menuHaschild;
	}
	public void setMenuHaschild(String menuHaschild) {
		this.menuHaschild = menuHaschild;
	}
	public String getTemp() {
		return temp;
	}
	public void setTemp(String temp) {
		this.temp = temp;
	}
	
}
