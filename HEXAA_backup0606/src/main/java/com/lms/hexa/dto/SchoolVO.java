package com.lms.hexa.dto;

public class SchoolVO {
	
	private String schoolId;
	private String schoolName;
	private String schoolURL;
	private String schoolAddress;
	private String schoolType;
	private String schoolGrade;
	private String schoolGender;
	private String schoolNum;
	private String schoolDong;
	
	
	public String getSchoolDong() {
		return schoolDong;
	}
	public void setSchoolDong(String schoolDong) {
		this.schoolDong = schoolDong;
	}
	public String getSchoolId() {
		return schoolId;
	}
	public void setSchoolId(String schoolId) {
		this.schoolId = schoolId;
	}
	public String getSchoolName() {
		return schoolName;
	}
	public void setSchoolName(String schoolName) {
		this.schoolName = schoolName;
	}
	public String getSchoolURL() {
		return schoolURL;
	}
	public void setSchoolURL(String schoolURL) {
		this.schoolURL = schoolURL;
	}
	public String getSchoolAddress() {
		return schoolAddress;
	}
	public void setSchoolAddress(String schoolAddress) {
		this.schoolAddress = schoolAddress;
	}
	public String getSchoolType() {
		return schoolType;
	}
	public void setSchoolType(String schoolType) {
		this.schoolType = schoolType;
	}
	public String getSchoolGrade() {
		return schoolGrade;
	}
	public void setSchoolGrade(String schoolGrade) {
		this.schoolGrade = schoolGrade;
	}
	public String getSchoolGender() {
		return schoolGender;
	}
	public void setSchoolGender(String schoolGender) {
		this.schoolGender = schoolGender;
	}
	public String getSchoolNum() {
		return schoolNum;
	}
	public void setSchoolNum(String schoolNum) {
		this.schoolNum = schoolNum;
	}
	
	
}
